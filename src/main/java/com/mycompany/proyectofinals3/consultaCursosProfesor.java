    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nombreProfesor = jComboBox1.getSelectedItem().toString();

        for (Profesor e : ProyectoFinalS3.profesores) {

            if (e.getNombre().equals(nombreProfesor)) {
                profesorSeleccionado = e;
                break;
            }

        }
        
        for (Curso b : ProyectoFinalS3.Cursos) {

            if (b.Catedratico.equals(nombreProfesor)) {
                cursoSeleccionado=b;
                                break;
            }

        }

        
        if (cursoSeleccionado != null) {
            DefaultTableModel data = new DefaultTableModel(new String[]{ "Nombre"
            }, ProyectoFinalS3.Cursos.size());
            jTable1.setModel(data);
            int row = 0;

            for (Curso a : ProyectoFinalS3.Cursos) {
                jTable1.setValueAt(a.nombre, row, 0);
                posicion++;

                row++;

            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

